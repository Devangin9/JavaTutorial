public class Example2 {
	
	
	public static void main(String[] args) {

		int i = 10;
		System.out.println(i);

		double d = 11.25;
		String output = "value = " + d ;
		System.out.println(output);
		
		boolean c = false;
		if (c)
			output = "value = " + c ;
		else
			output = "value != " + !c ;
		System.out.println(output);
		
		
		
		
	}
}
